namespace CmdLineNet
{
	/// <summary>
	/// How to align help text.
	/// </summary>
	public enum HelpTextAlign
	{
		/// <summary>
		/// No alignment is done
		/// </summary>
		None,
		/// <summary>
		/// Help text is only aligned within groups.
		/// </summary>
		WithinGroups,
		/// <summary>
		/// Help text is aligned across all groups.
		/// </summary>
		AcrossAllGroups,
	}
}